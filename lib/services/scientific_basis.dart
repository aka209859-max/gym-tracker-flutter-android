【科学的根拠データベース】

## 1. 筋力向上率の基準値
- 初心者 (0-6ヶ月): 月+15-25% （根拠: Effect of high intensity study - 6週間で+31.2%のデータ）
- 中級者 (6-24ヶ月): 月+5-10% （根拠: ACSM 2009 - 経験者データとACSMモデルからの推定）
- 上級者 (24ヶ月以上): 月+1-3% （根拠: ACSM 2009 - ピリオダイゼーションモデルに基づく推定）
- 進捗ルール: 目標レップを1-2回超過達成で、負荷を +2-10% 増加 （根拠: ACSM Position Stand 2009）

## 2. 最適トレーニング頻度
- 原則: 総ボリュームが最重要。ボリューム等価の場合、頻度による筋力向上の有意差なし (p = 0.421)。（根拠: Grgic et al. 2018 Sports Medicine）
- ボリューム非等価の場合: 週2回 (ES 0.82), 週3回 (ES 0.93)。頻度増がボリューム増を可能にする。（根拠: Grgic et al. 2018）
- 筋肥大: 週2回は週1回より優位。（根拠: Schoenfeld et al. 2016 Sports Medicine）
- 部位別推奨: 上半身・多関節種目は週3回以上の高頻度が有効 (p = 0.004)。（根拠: Grgic et al. 2018）

## 3. プラトー期の定義と対策
- 定義: 主要種目の進捗が 3-4週間 停滞した状態。
- 打破方法1（新刺激）: 高度なテクニック導入（ドロップセット、クラスターセット等）。（根拠: Krzysztofik et al. 2019 IJERPH）
- 打破方法2（疲労管理）: ディロードの実施。「生理・心理的疲労を軽減し、回復を促進する期間」。（根拠: Bell et al. 2023 BMC Sports Sci）
- ディロード最適化: ボリューム（セット数）を30-50%削減し、強度（重量）は維持する。期間は1週間。（根拠: Bell et al. 2023）

## 4. 推奨トレーニングボリューム（部位別・週あたり）
- 原則: 筋力・筋肥大ともに「段階的用量反応関係」あり。ボリュームが多いほど効果大。（根拠: Schoenfeld et al. 2017 JSS; Ralston et al. 2017 Sports Med）
- 筋肥大（セット追加効果）: 1セット追加ごとに筋肥大ゲイン +0.37%。（根拠: Schoenfeld et al. 2017）
- 初心者: 週 10-12 セット / 部位 （根拠: Ralston et al. 2017 - 週5セット以下は非効果的）
- 中級・上級者: 週 12-20 セット / 部位 （根拠: Baz-Valle et al. 2022 J Human Kinetics）
- 過剰トレーニング閾値: 週20セット以上は、上腕三頭筋では有効 (p=0.01) だが、大腿四頭筋・上腕二頭筋では効果が頭打ちの可能性。（根拠: Baz-Valle et al. 2022）

## 5. 最適休息日数（同一筋群）
- 大筋群 (脚・背中・胸): 48-72時間 （根拠: 2024 Review - 下半身・多関節種目は回復が遅い）
- 小筋群 (肩・腕): 24-48時間 （根拠: 2024 Review - 上半身は回復が速い）
- 生理学的根拠: 筋タンパク質合成(MPS)が最大48時間持続。（根拠: Davies et al. 2024 Translational Sports Medicine）
- レベル別: 初心者はMPS持続時間が長く（48h+）、上級者は短い（<24h）。（根拠: Damas et al. 2016 Sports Medicine）

## 6. 推奨トレーニング強度
- 目的別（筋力向上）: 高強度（>60% 1RM）が必須。低強度より有意に優れている。（根拠: Schoenfeld et al. 2017 JSCR）
- 目的別（筋肥大）: 「限界までの努力」が重要。限界まで行えば、高強度でも低強度でも筋肥大は同等。（根拠: Schoenfeld et al. 2017）
- 限界（Failure）の要否:
  * 筋力: 不要（ES = -0.09, 有意差なし）。（根拠: Grgic et al. 2022 J Sport Health Sci）
  * 筋肥大（経験者のみ）: 限界まで行う方が有効 (ES = 0.15, p < 0.05)。（根拠: Grgic et al. 2022）

## 7. 年齢・性別・遺伝要因
- 年齢: 50歳以上でも高強度トレーニングで大幅な筋力向上が可能（例: レッグプレス +29%）。（根拠: Peterson et al. 2010 MSSE）
- 性別（筋肥大）: 相対的な筋肥大率に男女差なし。（根拠: 2024 PeerJ Meta-analysis）
- 性別（筋力）: 下半身（相対）は男女差なし。上半身（相対）は、女性の方が男性より有意に向上率が高い (ES = -0.60)。（根拠: Roberts et al. 2020 JSCR）
- 遺伝（個人差）: 「グローバル・ノンレスポンダー」は存在しない可能性が高い。停滞は「遺伝的限界」ではなく「プログラムのミスマッチ」と捉え、ボリューム・強度・頻度の変更を提案すべき。（根拠: Pickering & Kiely 2019 Sports Medicine）
